async def handle_fda(query: str) -> dict:
    await asyncio.sleep(0.1)  # simulate IO
    q = query.lower()
    if any(kw in q for kw in ["ยา", "พาราเซตามอล", "drug"]):
        data = _FDA_RESPONSES["ยา"]
    elif any(kw in q for kw in ["อาหาร", "food", "อย."]):
        data = _FDA_RESPONSES["อาหาร"]
    elif any(kw in q for kw in ["เครื่องสำอาง", "cosmetic"]):
        data = _FDA_RESPONSES["เครื่องสำอาง"]
    else:
        data = _FDA_RESPONSES["ยา"]
    return {**data, "confidence": 0.92 + random.uniform(0, 0.07)}


